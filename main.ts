function initOverworld () {
    universeB = overworld.createMap16(
    overworld.mapRow16(overworld.tilemap16(tilemap`level0`), overworld.tilemap16(tilemap`level40`), overworld.tilemap16(tilemap`level3`)),
    overworld.mapRow16(overworld.tilemap16(tilemap`level42`), overworld.tilemap16(tilemap`level44`), overworld.tilemap16(tilemap`level9`)),
    overworld.mapRow16(overworld.tilemap16(tilemap`level46`), overworld.tilemap16(tilemap`level48`), overworld.tilemap16(tilemap`level15`))
    )
    libertyIsland = overworld.createMap16(
    overworld.mapRow16(overworld.tilemap16(tilemap`level72`), overworld.tilemap16(tilemap`level73`), overworld.tilemap16(tilemap`level74`)),
    overworld.mapRow16(overworld.tilemap16(tilemap`level79`), overworld.tilemap16(tilemap`level80`), overworld.tilemap16(tilemap`level87`)),
    overworld.mapRow16(overworld.tilemap16(tilemap`level79`), overworld.tilemap16(tilemap`level86`), overworld.tilemap16(tilemap`level87`)),
    overworld.mapRow16(overworld.tilemap16(tilemap`level91`), overworld.tilemap16(tilemap`level92`), overworld.tilemap16(tilemap`level93`))
    )
    scene.setBackgroundColor(7)
    overworld.setOverworld16(universeB)
    overworld.setAnimationType(overworld.AnimationType.Scroll)
    overworld.setPlayerSprite(Hiro)
    overworld.loadMap(0, 0)
    overworld.setWallsBlockTransitions(true)
}
